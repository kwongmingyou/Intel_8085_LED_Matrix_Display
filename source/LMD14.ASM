	MVI D, 34			;move data 34 into register D

DISP1:	MOV A, M			;copy the contents of memory location into accumulator

ROTATE: RRC				;the contents of accumulator is rotated right by 1 unit
	DCR B				;the contents of register B is decrease by 1
	JNZ ROTATE			;Jump to ROTATE if not zero
	ANI 00000001B			;the contents of accumulator and data 0000 0001B are ANDed and the result is put in the accumulator
	OUT PORTB			;the output is shown into PORTB
	CALL CLK			;start the subroutine CLK
	LDA 3399H			;the contents of the memory location specified are transferred to the accumulator
	MOV B, A			;copy the contents of accumulator into register B
	INX H				;the contents of memory location pointed to by HL are incremented by one and the result is put in the memory location
	DCR D				;the contents of register D is decrease by 1
	JNZ DISP1			;Jump to DISP1 if not zero
	CALL CLK			;start subroutine CLK
	MOV A, C			;copy the contents of register C into accumulator
	OUT PORTA			;the OUTPUT is shown in PORTA
	INR B				;the contents of register B is increased by 1
	INR C				;the contents of register C is increased by 1
	POP H				;the contents of the memory location pointed by the stack pointer are copy to the register pair HL

DISP2:	MOV A, C			;the contents of the register C is copied into accumulator
