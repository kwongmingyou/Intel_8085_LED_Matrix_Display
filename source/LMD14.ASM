; ===============================
;/	SUBROUTINE TRANSMIT	/
;===============================

TRANSMIT:	PUSH H
		PUSH B
		MOV C, A
		MVI B, 10
		MVI A, 01000000B
		SIM
TX_LOOP:	MVI H, BAUDTIME
TX_LOOP1:	DCR H
		JNZ TX_LOOP1
		MOV A, C
		STC
		RAR
		MOV C, A
		RAR
		ANI 80H
		ORI 01000000B
		SIM
		DCR B
		JNZ TX_LOOP
		POP B
		POP H
		RET


; ===============================
;/	SUBROUTINE RECEIVED	/
;===============================

RECEIVED:	PUSH H			;Copy contents of register pair HL into stack
		PUSH B			;Copy contents of register pair BC into stack
		MVI B, 9		;move data 9 into register B
SI1:	RIM				;Input serial data through the SID line
	ORA A				;logivally OR the contents of a register with the contents of accumulator
	JM SI1				;jump to SI1
	MVI H, BAUDTIME1/2		;load address data BAUDTIME1/2 to register pair HL
SI2:	DCR H				;decrease the content of register pair HL by 1
	JNZ SI2				;Jump to SI2 if not zero
SI4:	MVI H, BAUDTIME1		;load address data BAUDTIME1 to register pair HL
SI3:	DCR H				;decrease the content of register pair HL by 1
	JNZ SI3				;jump to SI3 if not zero
	RIM				;input serial data through the SID line
	RAL				;the contents of accumulator is rotated left by one position through the carry flag
	DCR B				;the contents of register B is decrease by 1
	JZ RX_EXIT			;Jump to RX_EXIT if zero
	MOV A, C			;copy the contents of register C to accumulator
	RAR				;the contents of accumulator is rotated right by one posistion through the carry flag
	MOV C, A			;copy the contents of accumulator to register C
	JMP SI4				;jump to SI4

RX_EXIT:	MOV A, C		;copy the content of register C into accumulator
		POP B			;the content of the memory location pointed by the stack pointer are copy to the register pair BC
		POP H			;the content of the memory location pointed by the stack pointer are copy to the register pair HL
		RET

		

